let currentLang = localStorage.getItem('fl_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('fl_lang', lang);

  // Update all translatable elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
      el.textContent = TRANSLATIONS[lang][key];
    }
  });

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
  });
  const langMap = { en:'EN', hi:'हि', te:'తె', ta:'த', kn:'ಕ' };
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.textContent.trim() === langMap[lang]) btn.classList.add('active');
  });

  // Update html lang attribute
  document.documentElement.lang = lang;
}

// Apply saved language on page load
document.addEventListener('DOMContentLoaded', () => {
  if (currentLang !== 'en') setLang(currentLang);
});
